def sig_label(p):
    if p < 0.001:  return '***'
    elif p < 0.01: return '**'
    elif p < 0.05: return '*'
    else:          return 'ns'


def bh_pairwise(data, metric, group_col, groups):
    """Kruskal-Wallis + pairwise Mann-Whitney U with BH correction."""
    gdata = {g: data.loc[data[group_col] == g, metric].dropna().values
             for g in groups}
    pairs = list(combinations(groups, 2))
    raw   = []
    for g1, g2 in pairs:
        if len(gdata[g1]) < 3 or len(gdata[g2]) < 3:
            raw.append(1.0)
        else:
            _, p = stats.mannwhitneyu(gdata[g1], gdata[g2], alternative='two-sided')
            raw.append(p)

    n     = len(raw)
    order = np.argsort(raw)
    bh    = np.zeros(n)
    for rank, idx in enumerate(order):
        bh[idx] = min(raw[idx] * n / (rank + 1), 1.0)
    for i in range(n - 2, -1, -1):
        bh[order[i]] = min(bh[order[i]], bh[order[i + 1]])

    valid = [d for d in gdata.values() if len(d) >= 3]
    _, kw_p = stats.kruskal(*valid) if len(valid) >= 2 else (np.nan, np.nan)

    return kw_p, {pairs[i]: {'raw': raw[i], 'bh': bh[i]} for i in range(n)}


