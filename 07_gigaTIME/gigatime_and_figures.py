# ============================================================================
#  Global color scale
# ============================================================================

def compute_global_scale(patients):
    C      = CONFIG
    ds     = C['macro_ds']
    roi_px = C['roi_size_px']
    roi_ds = roi_px // ds
    all_d  = []

    print("Computing global color scale from stromal CD8 distances...")
    for grp, pid in patients.items():
        pd_    = load_patient(pid)
        mh, mw = pd_['ck_ds'].shape
        H_c, W_c = pd_['dapi_f'].shape
        rois_ds = find_three_rois(pd_['ck_ds'], pd_['tg_ds'],
                                   pd_['cd8_ds'], roi_ds)
        for r0_ds, c0_ds in rois_ds:
            r0_ds = max(0, min(r0_ds, mh - roi_ds))
            c0_ds = max(0, min(c0_ds, mw - roi_ds))
            r0_c  = max(0, min(r0_ds * ds, H_c - roi_px))
            c0_c  = max(0, min(c0_ds * ds, W_c - roi_px))
            cd8_r = pd_['cd8_f'][r0_c:r0_c + roi_px, c0_c:c0_c + roi_px]
            ck_r  = pd_['ck_f'][r0_c:r0_c + roi_px, c0_c:c0_c + roi_px]
            tg_r  = pd_['tg_f'][r0_c:r0_c + roi_px, c0_c:c0_c + roi_px]
            _, dists, _ = stromal_cd8_centroids_and_distances(
                cd8_r, ck_r, tg_r, C['min_cell_px'])
            if dists is not None and len(dists) > 0:
                all_d.extend(dists.tolist())

    if not all_d:
        print("  Warning: no stromal distances found — using defaults")
        return 0.0, 200.0
    all_d = np.array(all_d)
    vmin  = float(np.percentile(all_d, 2))
    vmax  = float(np.percentile(all_d, 98))
    print(f"  Global scale: vmin={vmin:.1f} px  vmax={vmax:.1f} px")
    return vmin, vmax

