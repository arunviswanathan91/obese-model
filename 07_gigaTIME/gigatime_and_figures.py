def process_patient_sections(sdir, all_coords, model, device):
    """
    Phase 1 — geometric filter + fast CK probe on all sections (cheap)
    Phase 2 — full canvas only on the single best section (expensive)
    """
    sections = find_tissue_sections(all_coords, patch_size=512)
    print(f"  Sections detected: {len(sections)}")

    candidates = []

    for sec_i, indices in enumerate(sections):

        # Size filter
        if len(indices) < MIN_PATCHES:
            print(f"    Section {sec_i}: {len(indices)} patches — too small, skipped")
            continue

        sec_coords = [all_coords[i] for i in indices]
        xs = [x for x, y in sec_coords]
        ys = [y for x, y in sec_coords]
        W  = max(xs) + 512 - min(xs)
        H  = max(ys) + 512 - min(ys)
        canvas_gb = 23 * H * W * 2 / 1e9

        # Geometric filter - drop sparse debris
        tissue_density = len(indices) / ((W * H) / (512 * 512))
        if tissue_density < 0.3:
            print(f"    Section {sec_i}: tissue density {tissue_density:.2f} "
                  f"— sparse debris, skipped")
            continue

        # Fast CK probe
        probe_ck = probe_section_ck(sdir, sec_coords, model, device)
        print(f"    Section {sec_i}: {len(indices)} patches | "
              f"probe CK={probe_ck:.4f} | ~{canvas_gb:.1f} GB")

        if probe_ck < MIN_CK:
            print(f"    Section {sec_i}: probe CK below threshold — skipped")
            continue

        candidates.append((sec_i, sec_coords, probe_ck, canvas_gb))

    if not candidates:
        print(f"  No valid tumor sections found")
        return None, None, None

    # Sort by probe CK — best section first
    candidates.sort(key=lambda x: -x[2])
    best_sec_i, best_coords, best_probe_ck, best_gb = candidates[0]

    print(f"  → Best section: {best_sec_i} "
          f"(probe CK={best_probe_ck:.4f}, ~{best_gb:.1f} GB) "
          f"— building full canvas...")

    binary_canvas, scores, bbox = run_gigatime_on_section(
        sdir, best_coords, model, device
    )

    return binary_canvas, scores, bbox


