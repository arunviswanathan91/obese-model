# Albumentations pipeline: resize 256->512 then ImageNet normalise
_upsample_transform = Compose([
    A.Resize(512, 512),
    A.Normalize(),
])


def run_gigatime_on_section(series_dir, section_coords, model, device,
                             window_size=256, stride=128, tissue_thr=220):
    """
    Ran GigaTIME over the section defined by section_coords using a
    continuous stride loop and global logit canvas.

    Parameters
    ----------
    series_dir      : path to the DICOM series directory
    section_coords  : list of (x, y) top-left coords of the coarse 512-px
                      tiles that belong to the selected section
                      (output of select_best_section)
    model           : loaded GigaTIME in eval mode
    device          : 'cuda' or 'cpu'
    window_size     : WSI pixels per side extracted per step (256)
    stride          : step size in WSI pixels (128 = 50% overlap)
    tissue_thr      : mean pixel brightness above which a patch is background

    Returns
    -------
    binary_canvas : np.ndarray bool  (23, H, W)  — 1 pixel = 1 WSI pixel
    scores        : dict {protein: activation_density} - comparable across patients
    bbox          : (x_min, y_min, x_max, y_max) in WSI pixel coords
                    - anchor for downstream spatial graphs
    """
    # ── Bounding box from coarse tile coordinates ────────────────────────────
    xs = [x for x, y in section_coords]
    ys = [y for x, y in section_coords]
    x_min, y_min = min(xs), min(ys)
    x_max = max(xs) + 512
    y_max = max(ys) + 512
    W, H = x_max - x_min, y_max - y_min

    mem_gb = 23 * H * W * 2 / 1e9
    n_windows = ((H - window_size) // stride + 1) * ((W - window_size) // stride + 1)
    print(f"  Section bbox   : {W} x {H} px  ({W*0.5e-3:.1f} x {H*0.5e-3:.1f} mm at 20x)")
    print(f"  Canvas memory  : ~{mem_gb:.2f} GB  (float16 x 23 ch)")
    print(f"  Windows        : {n_windows}  (stride={stride} px, {stride/window_size*100:.0f}% step)")

    # ── Initialise global canvas on CPU ─────────────────────────────────────
    logit_canvas = torch.zeros(23, H, W, dtype=torch.float16)
    count_canvas = torch.zeros( 1, H, W, dtype=torch.float16)

    # ── Continuous stride loop ───────────────────────────────────────────────
    model.eval()
    wsi      = WsiDicom.open(series_dir)
    n_tissue = 0

    try:
        with torch.no_grad():
            pbar = tqdm_nb(total=n_windows, desc='  canvas', leave=True)
            for y in range(y_min, y_max - window_size + 1, stride):
                for x in range(x_min, x_max - window_size + 1, stride):
                    pbar.update(1)

                    # Read 256x256 patch from WSI
                    try:
                        region = wsi.read_region((x, y), 0, (window_size, window_size))
                        arr    = np.array(region.convert('RGB'))  # uint8 (256,256,3)
                    except Exception:
                        continue

                    if arr.mean() >= tissue_thr:
                        continue  # background — no contribution to canvas

                    # ── Resolution round-trip ────────────────────────────────
                    # UP: 256->512 px (albumentations Resize + ImageNet Normalize)
                    aug = _upsample_transform(image=arr)
                    inp = torch.from_numpy(
                        aug['image'].astype('float32').transpose(2, 0, 1)
                    ).unsqueeze(0).to(device)         # (1, 3, 512, 512)

                    # Inference: (1, 23, 512, 512) logits
                    logits_512 = model(inp)            # raw logits

                    # DOWN: 512->256 px so 1 output pixel == 1 WSI pixel
                    logits_256 = F.interpolate(
                        logits_512,
                        size=(window_size, window_size),
                        mode='bilinear',
                        align_corners=False,
                    ).squeeze(0).to(dtype=torch.float16).cpu()  # (23, 256, 256)

                    # ── Stitch onto global canvas ────────────────────────────
                    cy, cx = y - y_min, x - x_min
                    logit_canvas[:, cy:cy+window_size, cx:cx+window_size] += logits_256
                    count_canvas[:,  cy:cy+window_size, cx:cx+window_size] += 1
                    n_tissue += 1

                    if n_tissue % 500 == 0:
                        torch.cuda.empty_cache()

            pbar.close()

    finally:
        wsi.close()

    print(f"  Tissue windows : {n_tissue} / {n_windows}  ({n_tissue/n_windows*100:.1f}% tissue)")

    # ── Final aggregation ────────────────────────────────────────────────────


    safe_counts   = count_canvas.clamp(min=1)
    covered       = (count_canvas.squeeze(0) > 0).numpy()
    binary_canvas = np.zeros((23, H, W), dtype=np.uint8)
    scores        = {}

    for i, name in enumerate(CHANNEL_NAMES):
        avg_ch = (logit_canvas[i] / safe_counts[0]).float()  # (H, W)
        binary_canvas[i] = (torch.sigmoid(avg_ch) > 0.5).numpy().astype(np.uint8)
        if name not in EXCLUDE:
            scores[name] = float(binary_canvas[i][covered].mean()) if covered.any() else 0.0

    del logit_canvas, count_canvas, safe_counts, avg_ch
    torch.cuda.empty_cache()
    gc.collect()

    return binary_canvas, scores, (x_min, y_min, x_max, y_max)


