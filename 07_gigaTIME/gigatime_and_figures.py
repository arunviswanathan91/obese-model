def build_figure(annotated=True):
    C     = CONFIG
    n_grp = len(BMI_GROUPS)

    # Figure size — width and height both scale from cell dimensions
    total_col_gap = ((N_PER_GROUP - 1) * C['col_gap'] + (n_grp - 1) * C['group_gap']) * n_grp / n_grp
    # More precisely:
    total_gap_w = (N_PER_GROUP - 1) * n_grp * C['col_gap'] + (n_grp - 1) * C['group_gap']
    fig_w = C['left_margin'] + N_COLS * C['cell_w'] + total_gap_w + C['right_margin']
    fig_h = C['top_margin']  + N_ROWS * C['cell_h'] + (N_ROWS - 1) * C['row_gap'] + C['bottom_margin']

    fig = plt.figure(figsize=(fig_w, fig_h), facecolor=C['bg'])

    # Normalised geometry
    cell_w_f  = C['cell_w']        / fig_w
    cell_h_f  = C['cell_h']        / fig_h
    col_gap_f = C['col_gap']       / fig_w
    grp_gap_f = C['group_gap']     / fig_w
    row_gap_f = C['row_gap']       / fig_h
    l_f       = C['left_margin']   / fig_w
    b_f       = C['bottom_margin'] / fig_h
    t_f       = 1.0 - C['top_margin'] / fig_h

    # Column left edges
    col_x = []
    x = l_f
    for gi in range(n_grp):
        for pi in range(N_PER_GROUP):
            col_x.append(x)
            if pi < N_PER_GROUP - 1:
                x += cell_w_f + col_gap_f
            else:
                x += cell_w_f + grp_gap_f

    # Row bottom edges (row 0 = topmost displayed row)
    row_y = [b_f + (N_ROWS - 1 - ri) * (cell_h_f + row_gap_f)
             for ri in range(N_ROWS)]

    # Draw cells
    for ci, (grp, pid) in enumerate(COL_INFO):
        pd_ = pdata[pid]

        for ri, (key, _label, has_contour, is_he) in enumerate(ROWS):
            ax = fig.add_axes([col_x[ci], row_y[ri], cell_w_f, cell_h_f])
            ax.set_facecolor(C['bg'])
            ax.set_xticks([])
            ax.set_yticks([])
            for sp in ax.spines.values():
                sp.set_visible(False)

            if is_he:
                he = pd_['he']
                if he is not None:
                    ax.imshow(he, aspect='equal', interpolation='lanczos')
                    gray  = he.mean(axis=2)
                    tis   = gray < 210
                    fil   = binary_fill_holes(tis)
                    lb, n = nd_label(fil)
                    if n > 0:
                        sz   = [(lb == i).sum() for i in range(1, n + 1)]
                        mask = (lb == np.argmax(sz) + 1).astype(float)
                        sm   = gaussian_filter(mask, sigma=4)
                        ax.contour(sm, levels=[0.5],
                                   colors=['white'], linewidths=[0.8],
                                   linestyles=[(0, (5, 4))])
                else:
                    ax.text(0.5, 0.5, 'H&E\nunavailable',
                            ha='center', va='center',
                            color='#555', fontsize=9,
                            transform=ax.transAxes)
                continue

            img = pd_[key]
            H_img, W_img = img.shape[:2]
            ax.imshow(img, aspect='auto', interpolation='nearest')
            if has_contour:
                draw_contour(ax, pd_['dapi_ds'], H_img, W_img)

    # Annotations
    if annotated:

        # Group header + underline spanning 3 columns
        for gi, grp in enumerate(BMI_GROUPS):
            ci0 = gi * N_PER_GROUP
            ci1 = ci0 + N_PER_GROUP - 1
            x0  = col_x[ci0]
