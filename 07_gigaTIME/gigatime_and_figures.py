def violin_strip(ax, data, metric, group_col, groups, ylabel, title,
                 panel_letter, cfg):
    C = cfg

    gdata = [data.loc[data[group_col] == g, metric].dropna().values
             for g in groups]

    # --- Violin ---
    parts = ax.violinplot(
        [d if len(d) > 0 else [0] for d in gdata],
        positions=range(len(groups)),
        showmedians=False, showextrema=False,
        widths=C['violin_width'],
    )
    for pc, grp in zip(parts['bodies'], groups):
        pc.set_facecolor(PALETTE[grp])
        pc.set_edgecolor(C['violin_edge_color'])
        pc.set_linewidth(C['violin_edge_lw'])
        pc.set_alpha(C['violin_alpha'])

    # --- Median line ---
    for i, d in enumerate(gdata):
        if len(d) > 0:
            ax.hlines(np.median(d),
                      i - C['median_half_span'],
                      i + C['median_half_span'],
                      colors=C['median_color'],
                      linewidths=C['median_lw'],
                      zorder=5)

    # --- Jitter ---
    rng = np.random.default_rng(seed=C['jitter_seed'])
    for i, (d, grp) in enumerate(zip(gdata, groups)):
        if len(d) == 0:
            continue
        jitter = rng.uniform(-C['jitter_spread'], C['jitter_spread'], size=len(d))
        ax.scatter(i + jitter, d,
                   s=C['jitter_s'], alpha=C['jitter_alpha'],
                   color=PALETTE[grp],
                   edgecolors=C['jitter_edge_color'],
                   linewidths=C['jitter_edge_lw'],
                   zorder=6)

    # --- Statistics ---
    kw_p, pw = bh_pairwise(data, metric, group_col, groups)

    all_vals  = np.concatenate([d for d in gdata if len(d) > 0])
    y97       = np.percentile(all_vals, 97)
    y03       = np.percentile(all_vals, 3)
    y_range   = y97 - y03
    current_y = y97 + y_range * C['sig_gap_frac']

    pair_order = sorted(
        pw.keys(),
        key=lambda p: abs(groups.index(p[1]) - groups.index(p[0]))
    )

    for g1, g2 in pair_order:
        pdict   = pw[(g1, g2)]
        p_use   = pdict[C['sig_correction']]
        p_raw   = pdict['raw']
        lbl     = sig_label(p_use)

        if lbl == 'ns':
            continue

        x1, x2  = groups.index(g1), groups.index(g2)
        bar_h    = y_range * C['sig_bar_h_frac']
        x_mid    = (x1 + x2) / 2

        # Bracket
        ax.plot([x1, x1, x2, x2],
                [current_y, current_y + bar_h,
                 current_y + bar_h, current_y],
                lw=C['sig_bar_lw'], color=C['sig_bar_color'])

        # Star
        ax.text(x_mid, current_y + bar_h * 1.3,
                lbl,
                ha='center', va='bottom',
                fontsize=C['sig_star_fontsize'],
                fontweight='bold',
                color=C['sig_bar_color'])

        # Exact p-value on the line below the star
        p_str = f'p = {p_use:.3f}' if p_use >= 0.001 else 'p < 0.001'
        ax.text(x_mid, current_y + bar_h * 1.3 +
                C['sig_star_fontsize'] * 0.55,   # small offset below star
                p_str,
                ha='center', va='bottom',
                fontsize=C['sig_pval_fontsize'],
                color=C['sig_pval_color'])

        current_y += y_range * C['sig_step_frac']

    # --- x tick labels (group name + n) ---
    ax.set_xticks(range(len(groups)))
    ax.set_xticklabels(
        [f'{g}\n(n={len(d)})' for g, d in zip(groups, gdata)],
        fontsize=C['xlabel_fontsize']
    )

    # --- Axes labels and title ---
    ax.set_ylabel(ylabel, fontsize=C['ylabel_fontsize'])
    ax.set_title(f'{panel_letter}   {title}',
                 fontsize=C['title_fontsize'],
                 fontweight=C['title_fontweight'],
                 pad=C['title_pad'])

    # --- KW p-value annotation — only text inside plot ---
    kw_str = f'Kruskal-Wallis p = {kw_p:.3f}'
    ax.text(C['kw_x'], C['kw_y'], kw_str,
            transform=ax.transAxes,
            fontsize=C['kw_fontsize'],
            ha='right', va='top',
            color=C['kw_color'],
            style=C['kw_style'],
            fontweight=C['kw_weight'])

    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    ax.tick_params(axis='y', labelsize=C['ytick_fontsize'])

    return kw_p, pw


