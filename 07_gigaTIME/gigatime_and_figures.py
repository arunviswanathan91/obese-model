def tissue_silhouette(dapi_ds):
    C      = CONFIG
    extra  = C['contour_extra_ds']
    n_it   = C['contour_close_iter']
    d      = dapi_ds[::extra, ::extra] > 0
    struct = generate_binary_structure(2, 2)
    closed = binary_closing(d, structure=struct, iterations=n_it)
    filled = binary_fill_holes(closed)
    lbl, n = nd_label(filled)
    if n == 0:
        return filled.astype(np.float32)
    sizes  = [(lbl == i).sum() for i in range(1, n + 1)]
    blob   = (lbl == np.argmax(sizes) + 1).astype(np.float32)
    sigma  = max(2, blob.shape[0] // 40)
    return gaussian_filter(blob, sigma=sigma)


def draw_contour(ax, dapi_ds, H_img, W_img):
    if not CONFIG['tissue_contour']:
        return
