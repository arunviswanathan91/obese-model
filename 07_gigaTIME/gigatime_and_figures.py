# ── 2. FAST CK PROBE ──────────────────────────────────────────────────────────
def probe_section_ck(sdir, sec_coords, model, device, n_sample=30):
    """
    Fast CK probe — runs GigaTIME on a random sample of patches.
    Uses identical preprocessing to run_gigatime_on_section.
    ~2 seconds vs ~10 minutes for full canvas.
    """
    sample_coords = random.sample(sec_coords, min(n_sample, len(sec_coords)))
    patches = []

    wsi = WsiDicom.open(sdir)
    try:
        for x, y in sample_coords:
            try:
                region = wsi.read_region((x, y), 0, (256, 256))
                arr = np.array(region.convert('RGB'))
                if arr.mean() < 220:
                    patches.append(arr)
            except:
                continue
    finally:
        wsi.close()

    if len(patches) < 5:
        return 0.0

    # Identical transform to run_gigatime_on_section
    batch = torch.stack([
        torch.from_numpy(
            _upsample_transform(image=p)['image']
            .astype('float32').transpose(2, 0, 1)
        ) for p in patches
    ]).to(device)

    with torch.no_grad():
        logits     = model(batch)
        probs      = torch.sigmoid(logits)
        ck_density = probs[:, CK_IDX, :, :].mean().item()

    del batch, logits, probs
    torch.cuda.empty_cache()

    return ck_density


