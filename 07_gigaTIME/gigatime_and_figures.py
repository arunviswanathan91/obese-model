# ============================================================================
#  OLS regression + 95% CI band
# ============================================================================

def ols_line_ci(x, y, n_pts=200):
    mask = ~(np.isnan(x) | np.isnan(y))
    x, y = x[mask], y[mask]
    if len(x) < 4:
        return None, None, None, None
    slope, intercept, r, p, se = stats.linregress(x, y)
    x_fit  = np.linspace(x.min(), x.max(), n_pts)
    y_fit  = slope * x_fit + intercept
    n      = len(x)
    x_mean = x.mean()
    ss_x   = ((x - x_mean) ** 2).sum()
    s_err  = np.sqrt(((y - (slope * x + intercept)) ** 2).sum() / (n - 2))
    t_crit = stats.t.ppf(0.975, df=n - 2)
    ci     = t_crit * s_err * np.sqrt(1/n + (x_fit - x_mean)**2 / ss_x)
    return x_fit, y_fit, y_fit - ci, y_fit + ci

