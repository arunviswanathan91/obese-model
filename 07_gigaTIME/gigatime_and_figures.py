def find_wsi_dir(pid):
    rjson = os.path.join(SCORES_DIR, 'best_series_resnet.json')
    if os.path.exists(rjson):
        with open(rjson) as f:
            best = json.load(f)
        if pid in best and os.path.exists(best[pid]):
            return best[pid]
    matches = glob.glob(
        os.path.join(WSI_DIR, '**', pid, '**', '*.dcm'), recursive=True)
    return os.path.dirname(matches[0]) if matches else None


def load_he(pid):
    try:
        from wsidicom import WsiDicom
        d = find_wsi_dir(pid)
        if d is None:
            print(f"    No WSI dir: {pid}")
            return None
        wsi   = WsiDicom.open(d)
        thumb = wsi.read_thumbnail(size=CONFIG['he_thumb_size'])
        wsi.close()
        arr   = np.array(thumb.convert('RGB'))
        white = np.all(arr > 210, axis=2)
        tis   = ~white
        if tis.sum() < 100:
            return arr
        fil   = binary_fill_holes(tis)
        lb, n = nd_label(fil)
        if n == 0:
            return arr
        sz    = [(lb == i).sum() for i in range(1, n + 1)]
        blob  = lb == (np.argmax(sz) + 1)
        rows  = np.any(blob, axis=1)
        cols  = np.any(blob, axis=0)
        r0, r1 = np.where(rows)[0][[0, -1]]
        c0, c1 = np.where(cols)[0][[0, -1]]
        H, W  = arr.shape[:2]
        pad   = max(5, int((r1 - r0) * 0.05))
        r0 = max(0, r0-pad); r1 = min(H, r1+pad)
        c0 = max(0, c0-pad); c1 = min(W, c1+pad)
        crop  = arr[r0:r1, c0:c1]
        ch, cw = crop.shape[:2]
        side  = max(ch, cw)
        sq    = np.full((side, side, 3), 255, dtype=np.uint8)
        sq[(side-ch)//2:(side-ch)//2+ch,
           (side-cw)//2:(side-cw)//2+cw] = crop
        return sq
    except Exception as e:
        print(f"    H&E failed {pid}: {e}")
        return None

