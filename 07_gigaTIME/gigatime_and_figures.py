all_stats         = []
error_log         = []
significant_count = 0
SIG_THR           = CONFIG['sig_threshold']

for i, (ds_key, metric, ylabel, title, category) in enumerate(METRIC_DEFS):

    if ds_key not in datasets:
        error_log.append(f"Dataset '{ds_key}' not loaded — skipping {metric}")
        continue

    df_ = datasets[ds_key]

    if metric not in df_.columns:
        error_log.append(f"Column '{metric}' not in {ds_key} — skipping")
        continue

    valid = df_[[metric, 'BMI']].dropna()
    if len(valid) < 5:
        error_log.append(f"{metric}: only {len(valid)} valid rows — skipping")
        continue

    # ── Pre-compute stats once ───────────────────────────────────────────────
    x = valid['BMI'].values.astype(float)
    y = valid[metric].values.astype(float)

    sp_r, sp_p             = spearmanr(x, y)
    pe_r, pe_p             = pearsonr(x, y)
    slope, intercept, r_lin, p_lin, se = stats.linregress(x, y)

    stat_dict = {
        'metric'       : metric,
        'category'     : category,
        'dataset'      : ds_key,
        'n'            : len(valid),
        'spearman_r'   : round(sp_r, 4),
        'spearman_p'   : round(sp_p, 4),
        'pearson_r'    : round(pe_r, 4),
        'pearson_p'    : round(pe_p, 4),
        'ols_slope'    : round(slope, 6),
        'ols_intercept': round(intercept, 4),
        'ols_p'        : round(p_lin, 4),
        'ols_r2'       : round(r_lin**2, 4),
    }
    all_stats.append(stat_dict)

    # ── Significance filter ──────────────────────────────────────────────────
    is_significant = (sp_p < SIG_THR) or (pe_p < SIG_THR) or (p_lin < SIG_THR)
    if not is_significant:
        continue

    # ── Build figure ─────────────────────────────────────────────────────────
    significant_count += 1

    fig, ax = plt.subplots(
        figsize=(CONFIG['fig_width'], CONFIG['fig_height']),
        facecolor=CONFIG['fig_facecolor']
    )

    # Pass
    plot_continuous(ax, df_, metric, ylabel, title, CONFIG,
                    pre_stats=stat_dict)

    plt.tight_layout(pad=CONFIG['tight_layout_pad'])

    safe_metric = metric.replace('/', '-').replace(' ', '_')
    stem = f'{category.lower().replace(" ", "_")}_{safe_metric}'
    save_fig(fig, stem)
    plt.show()
    plt.close(fig)
    if significant_count % 5 == 0:
        print(f"  {significant_count} significant figures saved "
              f"(screened {i+1}/{len(METRIC_DEFS)})...")

