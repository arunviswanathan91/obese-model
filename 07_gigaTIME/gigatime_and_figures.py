def violin_strip(ax, data, metric, groups, ylabel, title, cfg):
    C     = cfg
    gdata = [data.loc[data[GROUP_COL]==g, metric].dropna().values for g in groups]
    parts = ax.violinplot([d if len(d)>0 else [0] for d in gdata],
                          positions=range(len(groups)), showmedians=False,
                          showextrema=False, widths=C['violin_width'])
    for pc, grp in zip(parts['bodies'], groups):
        pc.set_facecolor(PALETTE[grp]); pc.set_edgecolor(C['violin_edge_color'])
        pc.set_linewidth(C['violin_edge_lw']); pc.set_alpha(C['violin_alpha'])
    for i, d in enumerate(gdata):
        if len(d) > 0:
            ax.hlines(np.median(d), i-C['median_half_span'], i+C['median_half_span'],
                      colors=C['median_color'], linewidths=C['median_lw'], zorder=5)
    rng = np.random.default_rng(seed=C['jitter_seed'])
    for i, (d, grp) in enumerate(zip(gdata, groups)):
        if len(d) == 0: continue
        j = rng.uniform(-C['jitter_spread'], C['jitter_spread'], size=len(d))
        ax.scatter(i+j, d, s=C['jitter_s'], alpha=C['jitter_alpha'],
                   color=PALETTE[grp], edgecolors=C['jitter_edge_color'],
                   linewidths=C['jitter_edge_lw'], zorder=6)
    kw_p, pw = bh_pairwise(data, metric, GROUP_COL, groups)
    all_vals  = np.concatenate([d for d in gdata if len(d)>0])
    y97       = np.percentile(all_vals, 97)
    y_range   = np.percentile(all_vals, 97) - np.percentile(all_vals, 3)
    current_y = y97 + y_range * C['sig_gap_frac']
    for g1, g2 in sorted(pw.keys(),
                          key=lambda p: abs(groups.index(p[1])-groups.index(p[0]))):
        pdict = pw[(g1,g2)]; p_use = pdict[C['sig_correction']]
        lbl   = sig_label(p_use)
        if lbl == 'ns': continue
        x1, x2 = groups.index(g1), groups.index(g2)
        bar_h   = y_range * C['sig_bar_h_frac']; x_mid = (x1+x2)/2
        ax.plot([x1,x1,x2,x2],
                [current_y, current_y+bar_h, current_y+bar_h, current_y],
                lw=C['sig_bar_lw'], color=C['sig_bar_color'])
        ax.text(x_mid, current_y+bar_h*1.4, lbl, ha='center', va='bottom',
                fontsize=C['sig_star_fontsize'], fontweight=C['sig_star_fontweight'],
                color=C['sig_bar_color'])
        p_str = f'p = {p_use:.3f}' if p_use >= 0.001 else 'p < 0.001'
        ax.text(x_mid, current_y+bar_h*1.4+C['sig_star_fontsize']*0.6,
                p_str, ha='center', va='bottom', fontsize=C['sig_pval_fontsize'],
                color=C['sig_pval_color'], style=C['sig_pval_style'])
        current_y += y_range * C['sig_step_frac']
    ax.set_xticks(range(len(groups)))
    ax.set_xticklabels([f'{g}\n(n={len(d)})' for g, d in zip(groups, gdata)],
                       fontsize=C['xlabel_fontsize'])
    ax.set_ylabel(ylabel, fontsize=C['ylabel_fontsize'])
    ax.set_title(title, fontsize=C['title_fontsize'],
                 fontweight=C['title_fontweight'], pad=C['title_pad'])
    ax.text(C['kw_x'], C['kw_y'], f"{C['kw_prefix']}{kw_p:.3f}",
            transform=ax.transAxes, fontsize=C['kw_fontsize'],
            ha='right', va='top', color=C['kw_color'], style=C['kw_style'])
    ax.spines['top'].set_visible(False); ax.spines['right'].set_visible(False)
    ax.tick_params(axis='y', labelsize=C['ytick_fontsize'])

def build_panel(data, panels, suptitle, stem, ncols=3):
    C = CONFIG
    n = len(panels); nrows = int(np.ceil(n / ncols))
    fig_w = ncols * C['panel_col_width']
    fig_h = nrows * C['panel_row_height'] + C['panel_top_pad']
    fig, axes = plt.subplots(nrows, ncols, figsize=(fig_w, fig_h), squeeze=False)
    fig.suptitle(suptitle, fontsize=C['panel_suptitle_fontsize'],
                 fontweight=C['panel_suptitle_fontweight'], y=1.01)
    for idx, (metric, ylabel, title) in enumerate(panels):
        row, col = divmod(idx, ncols)
        ax = axes[row][col]
