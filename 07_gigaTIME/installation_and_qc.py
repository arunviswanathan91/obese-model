# Full dataset metrics
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from wsidicom import WsiDicom
from tqdm.notebook import tqdm
import logging, random

logging.getLogger('wsidicom').setLevel(logging.ERROR)

patch_size = 256
n_sample   = 200  # patches sampled per slide for stats

census = []

for _, row in tqdm(stats_df.iterrows(), total=len(stats_df), desc="Analyzing slides"):
    pid  = row['PatientID']
    sdir = row['best_sdir']
    size = row['size_mb']

    try:
        wsi = WsiDicom.open(sdir)
        w   = wsi.levels.base_level.size.width
        h   = wsi.levels.base_level.size.height

        all_pos = [
            (x, y)
            for y in range(0, h - patch_size, patch_size)
            for x in range(0, w - patch_size, patch_size)
        ]
        total_pos = len(all_pos)
        sampled   = random.sample(all_pos, min(n_sample, len(all_pos)))

        patch_means = []
        patch_stds  = []
        patch_r     = []   # red channel mean
        patch_g     = []   # green channel mean
        patch_b     = []   # blue channel mean

        for x, y in sampled:
            try:
                region = wsi.read_region((x, y), 0, (patch_size, patch_size))
                arr    = np.array(region.convert('RGB'))
                patch_means.append(arr.mean())
                patch_stds.append(arr.std())
                patch_r.append(arr[:,:,0].mean())
                patch_g.append(arr[:,:,1].mean())
                patch_b.append(arr[:,:,2].mean())
            except:
                continue
        wsi.close()

        patch_means = np.array(patch_means)
        patch_stds  = np.array(patch_stds)

        tissue_mask = patch_means < 220

        census.append({
            'PatientID'           : pid,
            'size_mb'             : size,
            'slide_w'             : w,
            'slide_h'             : h,
            'total_positions'     : total_pos,
            # Tissue coverage
            'tissue_frac'         : tissue_mask.mean().round(3),
            'est_tissue_patches'  : int(tissue_mask.mean() * total_pos),
            # Pixel intensity stats (all patches)
            'mean_pixel_all'      : patch_means.mean().round(1),
            'std_pixel_all'       : patch_means.std().round(1),
            # Tissue patches only
            'mean_pixel_tissue'   : patch_means[tissue_mask].mean().round(1) if tissue_mask.any() else None,
            'std_pixel_tissue'    : patch_stds[tissue_mask].mean().round(1)  if tissue_mask.any() else None,
            # Colour channels (tissue only) — H&E stain quality check
            'mean_R_tissue'       : np.array(patch_r)[tissue_mask].mean().round(1) if tissue_mask.any() else None,
            'mean_G_tissue'       : np.array(patch_g)[tissue_mask].mean().round(1) if tissue_mask.any() else None,
            'mean_B_tissue'       : np.array(patch_b)[tissue_mask].mean().round(1) if tissue_mask.any() else None,
            # Texture (high std = rich tissue, low std = fat/necrosis)
            'pct_high_texture'    : (patch_stds[tissue_mask] > 30).mean().round(3) if tissue_mask.any() else None,
        })

    except Exception as e:
        print(f"  ERROR {pid}: {e}")
        census.append({'PatientID': pid, 'size_mb': size})

census_df = pd.DataFrame(census)
census_df.to_csv(f'{SCORES_DIR}/dataset_census_full.csv', index=False)
