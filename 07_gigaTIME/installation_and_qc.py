# Classify each patient into: GOOD / WARN / BAD / ERROR #21
import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

def classify_slide(row):
    if pd.isna(row.get('tissue_frac')):
        return 'ERROR'   # wsidicom failed
    if row['tissue_frac'] < 0.03:
        return 'BAD'     # barely any tissue
    if row['pct_high_texture'] < 0.5:
        return 'BAD'     # mostly flat/artifact patches
    if row['tissue_frac'] < 0.07 or row['pct_high_texture'] < 0.7:
        return 'WARN'    # low but usable
    return 'GOOD'

